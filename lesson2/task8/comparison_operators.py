"""
Python tiene muchos tipos de operadores de comparación, incluidos
>=,
<=,
>,
<, etc.
Todas las operaciones de comparación en Python tienen la misma prioridad.
Las comparaciones arrojan valores booleanos: True o False.
Las comparaciones pueden encadenarse arbitrariamente.

"""
one = 1
two = 2
three = 3

print(one < two < three)

is_greater = three > two
print(is_greater)
